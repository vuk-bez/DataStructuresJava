package Red.Ulancan;

public class Stanja {
    int a;
    int b;
    int brojac;
    public Stanja(int a, int b, int brojac) {
        this.a = a;
        this.b = b;
        this.brojac = brojac;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    public int getBrojac() {
        return brojac;
    }

    public void setBrojac(int brojac) {
        this.brojac = brojac;
    }
}
